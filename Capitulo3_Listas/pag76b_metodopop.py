print("removendo um item com o método pop()")

motorcycles = ['honda','yamaha','suzuki']
print(motorcycles)

popped_motorcycles = motorcycles.pop()
print(motorcycles)
print("Item removido:")
print(popped_motorcycles)

print("O método pop() permite armazenar o último item em uma variável.")
last_owned = motorcycles.pop()
print("The last motorcycle I owned was a " 
      + last_owned.title() + ".")

