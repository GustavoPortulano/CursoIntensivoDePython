"""Removendo espaços em branco."""

"""Removendo espaços do lado direito:"""
favorite_language="python "
print("favorite_language")
favorite_language.rstrip()
print("favorite_language")

"""Removendo espaços do lado esquerdo:"""
segunda_linguagem="java  "   
segunda_linguagem.lstrip()
print("segunda_linguagem")

"""Removendo espaços de ambos os lados:"""
terceira_linguagem="javascript"
terceira_linguagem.strip()
print("terceira_linguagem")